class ViTReconstruction(nn.Module):
    def __init__(self, *, num_patches= 196, dim=768, depth=12, heads=12, mlp_dim=768*4,  dim_head = 64, dropout = 0., emb_dropout = 0.):
        super().__init__()
        self.patch_embed = PatchEmbedding()
        self.vit = ViT(num_patches=num_patches, dim=dim, depth=depth, heads=heads, mlp_dim=mlp_dim,  dim_head = 64, dropout = 0., emb_dropout = 0.)
        self.conv1 = nn.Conv2d(in_channels=3, out_channels=3, kernel_size=3, stride=1, padding=1)
        self.conv2 = nn.Conv2d(in_channels=3, out_channels=1, kernel_size=3, stride=1, padding=1)
    def unpatchify(self, x):
        """
        x: (N, L, 3*patch_size**2 )
        imgs: (N, 3, H, W)
        """
        # print(x.shape)
        # x = cv2.resize(x, (224, 224))
        p = 16 # self.patch_embed.patch_size[0]
        h = w = int(x.shape[1] ** .5) # 14
        assert h * w == x.shape[1]

        x = x.reshape(shape=(x.shape[0], h, w, p, p, 3)) # [N, 14, 14, 16, 16, 3]
        x = torch.einsum('nhwpqc->nchpwq', x) # [N, 3, 14, 16, 14, 16]
        imgs = x.reshape(shape=(x.shape[0], 3, h * p, h * p))
        return imgs # [B, 3, 224, 224]

    def forward(self, x):
        x = transforms.Resize((224, 224))(x)
        x = self.patch_embed(x)
        x = self.vit(x)
        x = self.unpatchify(x[:, 1: ,:])
        x = self.conv2(self.conv1(x))
        return x	


class EncoderNeibrTransformer(nn.Module):
    def __init__(self, img_size=256, in_channels=3, embed_dim=96): # 256 3 96
        super(EncoderNeibrTransformer, self).__init__()
        self.patch_embed = PatchEmbed(img_size=img_size, patch_size=4, in_channels=in_channels, embed_dim=embed_dim, norm_layer=nn.LayerNorm)
        self.block1 = NeibrBasicLayer(input_resolution=img_size//4, dim=embed_dim, depth=2, gap=2, expand=3) # 64
        self.patch_merging2 = PatchMerging(input_resolution=img_size//4, dim=embed_dim) # 64
        self.block2 = NeibrBasicLayer(input_resolution=img_size//8, dim=embed_dim*2, depth=2, gap=2, expand=2) # 32
        self.patch_merging3 = PatchMerging(input_resolution=img_size//8, dim=embed_dim*2) # 32
        self.block3 = NeibrBasicLayer(input_resolution=img_size//16, dim=embed_dim*4, depth=2, gap=1, expand=2) # 16
        self.patch_merging4 = PatchMerging(input_resolution=img_size//16, dim= embed_dim*4) # 16
        self.block4 = NeibrBasicLayer(input_resolution=img_size//32, dim=embed_dim*8, depth=1, gap=0, expand=4) # 8

    def forward(self, x):

        x = self.patch_embed(x)
        b1 = self.block1(x)
        pm1 = self.patch_merging2(b1)
        b2 = self.block2(pm1)
        pm2 = self.patch_merging3(b2)
        b3 = self.block3(pm2)
        pm3 = self.patch_merging4(b3)
        b4 = self.block4(pm3)

        return b4

class DecoderNeibrTransformer(nn.Module):
    def __init__(self, img_size=256, out_channels=3, embed_dim=96):
        super(DecoderNeibrTransformer, self).__init__()
        self.block1 = NeibrBasicLayer(input_resolution=img_size//32, dim=embed_dim*8, depth=1, gap=0, expand=4) # 8
        self.patch_expand1 = PatchExpand(input_resolution=img_size//32, dim=embed_dim*8) #16
        self.block2 = NeibrBasicLayer(input_resolution=img_size//16, dim=embed_dim*4, depth=1, gap=1, expand=2)
        self.patch_expand2 = PatchExpand(input_resolution=img_size//16, dim=embed_dim*4) # 32
        self.block3 = NeibrBasicLayer(input_resolution=img_size//8, dim=embed_dim*2 , depth=1, gap=2, expand=2)
        self.patch_expand3 = PatchExpand(input_resolution=img_size//8, dim=embed_dim*2) # 64
        self.block4 = NeibrBasicLayer(input_resolution=img_size//4, dim=embed_dim, depth=1, gap=2, expand=3)
        self.norm = nn.LayerNorm(embed_dim)
        self.proj = nn.ConvTranspose2d(in_channels=embed_dim, out_channels=out_channels, kernel_size=3, stride=4, padding=1, dilation=2, output_padding=1)

    def forward(self, x):
        b0 = x
        b1 = self.block1(b0)
        pe1 = self.patch_expand1(b1)
        b2 = self.block2(pe1)
        pe2 = self.patch_expand2(b2)
        b3 = self.block3(pe2)
        pe3 = self.patch_expand3(b3)
        b4 = self.block4(pe3)
        b4 = self.norm(b4)
        B, L, C = b4.shape
        b4 = b4.reshape(B, int(math.sqrt(L)), int(math.sqrt(L)), C).permute(0, 3, 1, 2)
        b5 = self.proj(b4)

        return b5



